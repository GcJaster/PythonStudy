def bubble_sort(array):
    swapped = False
    for i in range(len(array) - 1, 0, -1):
        for j in range(i):
            if array[j] > array[j + 1]:
                array[j], array[j + 1] = array[j + 1], array[j]
                swapped = True
        if swapped:
            swapped = False
        else:
            break
    return array


arr = [12, 11, -13, 0, 6]
print(f"Sorted array is: {bubbleSort(arr)}")
